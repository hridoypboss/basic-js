// inch to feet conversion function
function incToFeet(inch) { // inch to feet conversion function
    var feet = inch / 12;
    return feet;
}
var nane = incToFeet(120);

var dada = incToFeet(144);

var nana = incToFeet(60);
console.log(nane);   // output: 10
console.log(dada);  // output: 12
console.log(nana);    // output: 5